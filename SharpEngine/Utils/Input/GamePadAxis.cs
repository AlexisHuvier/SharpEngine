namespace SharpEngine.Utils.Input;

/// <summary>
/// Enum which represents Axis of Gamepad
/// </summary>
public enum GamePadAxis
{
    /// <summary>
    /// Left Stick X Axis
    /// </summary>
    LeftX,
    
    /// <summary>
    /// Left Stick Y Axis
    /// </summary>
    LeftY,
    
    /// <summary>
    /// Right Stick X Axis
    /// </summary>
    RightX,
    
    /// <summary>
    /// Right Stick Y Axis
    /// </summary>
    RightY,
    
    /// <summary>
    /// Left Trigger Axis
    /// </summary>
    LeftTrigger,
    
    /// <summary>
    /// Right Trigger Axis
    /// </summary>
    RightTrigger,
}